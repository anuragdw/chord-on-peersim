package peersim.chord;

import java.math.*;
import peersim.core.*;

public class ExpandSetMessage implements ChordMessage {


	private BigInteger targetId;
	Node node;


	public ExpandSetMessage(BigInteger targetId, Node node) {
		this.targetId = targetId;
		this.node = node;
		
	}

	public BigInteger getTargetId () {
		return targetId;
	}

	public Node getRequester() {
		return node;
	}
		

}
